#!/usr/bin/env python3
"""
This module contains a type-annotated function that adds two floats.
"""


def add(a: float, b: float) -> float:
    """
    Adds two float numbers.

    Args:
        a (float): The first float number.
        b (float): The second float number.

    Returns:
        float: The sum of the two float numbers.
    """
    return a + b
